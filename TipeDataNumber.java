public class TipeDataNumber {
    public static void main(String[] args) {
        byte tipeDataByte = 127;
        System.out.println(tipeDataByte);

        short tipeDataShort = 32767;
        System.out.println(tipeDataShort);


        int tipeDataInt = 2147483647;
        System.out.println(tipeDataInt);

        long tipeDataLong = 32767;
        System.out.println(tipeDataLong);


        float tipeDataFloat = 3.4F;
        System.out.println(tipeDataFloat);

        double tipeDataDouble = 3.4;
        System.out.println(tipeDataDouble);

        char tipeDataChar = 'A';
        System.out.println(tipeDataChar);


        int decimalint = 20;
        System.out.println(decimalint);
        int hexdecimal = 0xff;
        System.out.println(hexdecimal);
        int bindecimal = 0b0101;
        System.out.println(bindecimal);
        int jumlahUang = 1_000_000;
        System.out.println(jumlahUang);
    }
}
